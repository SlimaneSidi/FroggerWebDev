// Function to render the game board
function renderGameBoard() {
    var container = document.getElementById('gameBoard');
    for (var i = 0; i < gameBoard.length; i++) {
        for (var j = 0; j < gameBoard[i].length; j++) {
            var cell = document.createElement('div');
            cell.className = 'cell';
            switch(gameBoard[i][j]) {
                case 0:
                    cell.classList.add('empty');
                    break;
                case 1:
                    cell.classList.add('player1');
                    break;
                case 2:
                    cell.classList.add('player2');
                    break;
                case 3:
                    cell.classList.add('player3');
                    break;
                case 4:
                    cell.classList.add('player4');
                    break;
            }
            container.appendChild(cell);
        }
        container.appendChild(document.createElement('br')); // Line break after each row
    }
}

// Call the function to render the game board
renderGameBoard();
